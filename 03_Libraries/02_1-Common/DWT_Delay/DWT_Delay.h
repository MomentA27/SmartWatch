/**
 * @brief 系统核心时钟频率定义
 * @note  需根据实际硬件配置修改 CPU_FREQ_MHZ 的值。
 *        对于 S32K144，常见值为 48MHz、80MHz、112MHz 等。
 */
#define CPU_FREQ_MHZ     100UL   // CPU主频，单位 MHz
#define CPU_FREQ_HZ      (CPU_FREQ_MHZ * 1000000UL)  // CPU主频，单位 Hz

